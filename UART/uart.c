uint16 CRC16_Modbus(uint8 *_pBuf, uint16 _usLen)
{
	
	uint8 i;
	int8 n;
	uint16 crc = 0xffff;
	uint16 polynom = 0xA001;
	
	
	if (_usLen == 0) {
		_usLen = 1;
	}
	for (n = 0; n<_usLen; n++) {/*此处的6 -- 要校验的位数为6个*/
		crc = _pBuf[n] ^ crc;
		for (i = 0; i < 8; i++) {  /*此处的8 -- 指每一个char类型又8bit，每bit都要处理*/
			if (crc & 0x01) {
				crc = crc >> 1;
				crc = crc ^ 0xA001;
			}
			else {
				crc = crc >> 1;
			}
		}
	}
	return(crc);
}

uint16 BEBufToUint16(uint8 *_pBuf)
{
    return (((uint16)_pBuf[0] << 8) | _pBuf[1]);
}

void MODS_SendWithCRC(uint8 *_pBuf, uint8 _ucLen)
{
	uint16 crc=0xFFFF;
	uint8 buf[S_TX_BUF_SIZE];
	int8 n,i;
	
	memcpy(buf, _pBuf, _ucLen);

	for (n = 0; n<_ucLen; n++) {
		crc = buf[n] ^ crc;
		for (i = 0; i < 8; i++) {
			if (crc & 0x01) {
				crc = crc >> 1;
				crc = crc ^ 0xA001;
			}
			else {
				crc = crc >> 1;
			}
		}
	}
	buf[_ucLen++] = crc;
	buf[_ucLen++] = crc >> 8;
	
	RS485_SendBuf(buf, _ucLen);

}

void MODS_SendAckErr(uint8 _ucErrCode)
{
	uint8 txbuf[3];

	txbuf[0] = g_tModS.RxBuf[0];					
	txbuf[1] = g_tModS.RxBuf[1] | 0x80;		
	txbuf[2] = _ucErrCode;							

	MODS_SendWithCRC(txbuf, 3);
}


void RS485_SendBuf(uint8 *_ucaBuf, uint16 _usLen)
{
	uint16 ii,t;
	for(ii=0;ii<_usLen;ii++)
	{
		if(TEND)	  //允许发送
		{
			 TEND=0;
			 TBUF= _ucaBuf[t++&0x0F];
			 TING=1;
		}
		while(!TEND);
	}
	t=0;
	RS485_DIR=0;
	
}


