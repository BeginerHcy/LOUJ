void UART_CYC()
{

	if(RING)
	{
		if(--RCNT==0){
			RCNT=3;
			if(--RBIT==0){
				RBUF=RDAT;
				RING=0;
				REND=1;
			}
			else{
				RDAT>>=1;
				if(RXB) 
					RDAT|=0X80;
			}
		}
	}
	else if(!RXB){
		RING=1;
		RCNT=4;
		RBIT=9;
	}
	if(--TCNT==0)
	{
		TCNT=3;
		if(TING){
			if(TBIT==0){
				TXB=0;
				TDAT=TBUF;
				TBIT=9;
			}
			else
			{
				TDAT>>=1;
				if(--TBIT==0){
					TXB=1;
					TING=0;
					TEND=1;
				}
				else
					TXB = CY;
			}
		}
	}
}

///Read coil status
void MODS_01H(void)
{

	uint16 reg;
	uint16 num;
	uint16 i;
	uint16 m;
	uint8 status[10];
	
	g_tModS.RspCode = RSP_OK;

	if (g_tModS.RxCount != 8)
	{
		g_tModS.RspCode = RSP_ERR_VALUE;
		return;
	}

	reg = BEBufToUint16(&g_tModS.RxBuf[2]); 
	num = BEBufToUint16(&g_tModS.RxBuf[4]);	
	m = (num + 7) / 8;
	
	
	if(num>COIL_NUM){
		g_tModS.RspCode = RSP_ERR_REG_ADDR;
		goto err_ret;
	}
		
		
	for (i = 0; i < m; i++)
		status[i] = 0;
	
	for (i = 0; i < num; i++)
	{
		if(reg<(COIL_BASE+COIL_NUM)){
			if(g_tVar.Coils[reg-COIL_BASE]==1){
				status[i / 8] |= (1 << (i % 8));
			}
		}
		else{		
				g_tModS.RspCode = RSP_ERR_REG_ADDR;
				break;
		}
		reg++;
	}

	err_ret:
	if (g_tModS.RspCode == RSP_OK)
	{
		g_tModS.TxCount = 0;
		g_tModS.TxBuf[g_tModS.TxCount++] = g_tModS.RxBuf[0];
		g_tModS.TxBuf[g_tModS.TxCount++] = g_tModS.RxBuf[1];
		g_tModS.TxBuf[g_tModS.TxCount++] = m;	
		g_tModS.TxBuf[3] = 0;
		g_tModS.TxBuf[4] = 0;
		g_tModS.TxBuf[5] = 0;
		g_tModS.TxBuf[6] = 0;
		
		
		for (i = 0; i < m; i++)
		{
			g_tModS.TxBuf[g_tModS.TxCount++] = status[i];
		}
		MODS_SendWithCRC(g_tModS.TxBuf, g_tModS.TxCount);
	}
	else
	{
		;
		MODS_SendAckErr(g_tModS.RspCode);	
	}
}


