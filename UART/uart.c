void MODS_0FH(void)
{

	uint16 reg;
	uint16 _ByteNum=0;
	uint16 _coilnum;
	uint8 * p;
	int8 * varAdr;
	int8 ireg;

	g_tModS.RspCode = RSP_OK;
	
	if (g_tModS.RxCount < 8)
	{
		g_tModS.RspCode = RSP_ERR_VALUE;		/* error frame*/
		goto err_ret;
	}
	reg = BEBufToUint16(&g_tModS.RxBuf[2]); 	/*start address*/
	_coilnum = BEBufToUint16(&g_tModS.RxBuf[4]);	/* êy?Y */	
	_ByteNum = g_tModS.RxBuf[6];
	
	p = &g_tModS.RxBuf[7];
	
	varAdr = &g_tVar.Coils[reg-COIL_BASE];
	
	if ((reg+_coilnum)<(COIL_BASE+COIL_NUM))
	{
		for(ireg=0;ireg<_coilnum;ireg++){
			* (varAdr+ireg) = (*(p + (int)(ireg / 8) ) >> (int)(ireg % 8)) & (0x01);	
		}
	}
	else
	{
		g_tModS.RspCode = RSP_ERR_REG_ADDR;	
	}
err_ret:
	if (g_tModS.RspCode == RSP_OK)
	{
		MODS_SendAckOk();
	}
	else
	{
		MODS_SendAckErr(g_tModS.RspCode);
	}
}

void MODS_05H(void)
{
	uint16 reg;
	uint16 value;

	g_tModS.RspCode = RSP_OK;
	
	if (g_tModS.RxCount != 8)
	{
		g_tModS.RspCode = RSP_ERR_VALUE;
		goto err_ret;
	}

	reg = BEBufToUint16(&g_tModS.RxBuf[2]); 
	value = BEBufToUint16(&g_tModS.RxBuf[4]);	
	
	if (value != 0x0000 && value != 0xFF00)
	{
		g_tModS.RspCode = RSP_ERR_VALUE;
		goto err_ret;
	}
	
	if (reg<(COIL_BASE+COIL_NUM))
	{
		g_tVar.Coils[reg-COIL_BASE] = (value==0xFF00);
	}
	else
	{
		g_tModS.RspCode = RSP_ERR_REG_ADDR;	
	}
err_ret:
	if (g_tModS.RspCode == RSP_OK)
	{
		MODS_SendAckOk();
	}
	else
	{
		MODS_SendAckErr(g_tModS.RspCode);	
	}
}


